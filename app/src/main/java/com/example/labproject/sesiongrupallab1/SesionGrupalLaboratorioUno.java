package com.example.labproject.sesiongrupallab1;

public class SesionGrupalLaboratorioUno {
    private String Computadora;
    private String NoBoleta;
    private String AluNombre;
    private String AluApeP;
    private String AluApeM;
    private String SemestreAlu;

    public String getComputadora() {
        return Computadora;
    }

    public void setComputadora(String computadora) {
        Computadora = computadora;
    }

    public String getNoBoleta() {
        return NoBoleta;
    }

    public void setNoBoleta(String noBoleta) {
        NoBoleta = noBoleta;
    }

    public String getAluNombre() {
        return AluNombre;
    }

    public void setAluNombre(String aluNombre) {
        AluNombre = aluNombre;
    }

    public String getAluApeP() {
        return AluApeP;
    }

    public void setAluApeP(String aluApeP) {
        AluApeP = aluApeP;
    }

    public String getAluApeM() {
        return AluApeM;
    }

    public void setAluApeM(String aluApeM) {
        AluApeM = aluApeM;
    }

    public String getSemestreAlu() {
        return SemestreAlu;
    }

    public void setSemestreAlu(String semestreAlu) {
        SemestreAlu = semestreAlu;
    }
}
